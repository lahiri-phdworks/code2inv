int main() {
  // variable declarations
  int c;
  // scanf("%d", &c);
  // pre-conditions
  (c = 0);
  // precheck
  // loopcond : (unknown())
  // loopstart
  while (unknown())
  // loop body
  {
    {
      if (unknown()) {
        if ((c != 40)) {
          (c = (c + 1));
        }
      } else {
        if ((c == 40)) {
          (c = 1);
        }
      }
    }
  }
  // loopend
  // postcheck
  // post-condition
  if ((c < 0))
    if ((c > 40))
      assert((c == 40));
}
